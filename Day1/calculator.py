x = float(input("Enter value of x: "))
y = float(input("Enter value of y: "))

def sum(x,y):
    return x+y

def difference(x,y):
    return x-y

def product(x,y):
    return x*y

def divide(x,y):
    return x/y

print(f"Sum is {sum(x,y)}")
print(f"difference is {difference(x,y)}")
print(f"product is {product(x,y)}")
print(f"divide is {divide(x,y)}")